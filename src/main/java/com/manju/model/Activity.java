package com.manju.model;

public class Activity {
	
	double countA;
	double countB;
	double countC;
	double countD;
	double total;
	
	public double getCountA() {
		return countA;
	}
	public void setCountA(double countA) {
		this.countA = countA;
	}
	public double getCountB() {
		return countB;
	}
	public void setCountB(double countB) {
		this.countB = countB;
	}
	public double getCountC() {
		return countC;
	}
	public void setCountC(double countC) {
		this.countC = countC;
	}
	public double getCountD() {
		return countD;
	}
	public void setCountD(double countD) {
		this.countD = countD;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
}
